                                        <h2 id="where-to-display-terms-of-use">Where to Display Terms of Use?</h2>
                                        <p>
                                            There are different approaches when it comes to displaying terms of use on a website. One is to simply use a browsewrap agreement and display a hyperlink in the website footer titled “terms of
                                            use” which brings a user to a separate page where they can learn more about your policies.
                                        </p>
                                        <p>
                                            However, more and more businesses are requesting affirmative actions from website visitors and setting up a pop-up or checkbox requesting that the visitor agrees to the terms of use before using
                                            the website or product, like this:
                                        </p>
                                        <figure style="text-align: center;">
                                            <img
                                                data-src="https://websitepolicies.gumlet.io/uploads/4/x/h/e/7cz4tzsya4008f77fa2s.png"
